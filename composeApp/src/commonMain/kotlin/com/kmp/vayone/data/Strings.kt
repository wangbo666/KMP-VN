    fun setLang(lang: String) {
        language = lang
        CacheManager.setLanguage(lang)
    }

    operator fun get(key: String, vararg args: String): String {
        val dict = when (language) {
            "vi" -> vi
            else -> en
        }

        val text = dict[key] ?: key
        return if (args.isEmpty()) text
        else text.replace(Regex("\\{\\d+\\}")) {
            val index = it.value.removeSurrounding("{", "}").toInt()
            args.getOrNull(index) ?: it.value
        }
    }
